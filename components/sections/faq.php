        <div class="faq-container">
            <div class="faq-item">
                <button class="faq-question">What is digital sovereignty?
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" width="20" height="20"><path stroke-linecap="round" stroke-linejoin="round" d="M19.5 8.25l-7.5 7.5-7.5-7.5" /></svg>
                </button>
                <div class="faq-answer">
                    <p>Digital sovereignty means having control over your own digital infrastructure and data. For Europe, it means:</p>
                    <ul>
                        <li>European data stored on European servers under European law</li>
                        <li>Infrastructure owned and controlled by European companies</li>
                        <li>Protection from foreign surveillance and legal overreach</li>
                        <li>The ability to enforce European values and regulations without foreign interference</li>
                    </ul>
                </div>
            </div>
            <div class="faq-item">
                <button class="faq-question">Why can't European companies just use American cloud services?
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" width="20" height="20"><path stroke-linecap="round" stroke-linejoin="round" d="M19.5 8.25l-7.5 7.5-7.5-7.5" /></svg>
                </button>
                <div class="faq-answer">
                    <p>American cloud services subject European data to US law, specifically:</p>
                    <ul>
                        <li><strong>CLOUD Act:</strong> Allows US government to demand data from US companies regardless of where it's stored</li>
                        <li><strong>FISA Section 702:</strong> Permits warrantless surveillance of non-US persons</li>
                        <li><strong>Executive Order 12333:</strong> Authorizes broad intelligence collection outside US territory</li>
                    </ul>
                    <p>These laws directly contradict GDPR and European privacy rights. The EU Court of Justice has repeatedly ruled that US law doesn't provide adequate protection (Schrems I &amp; II decisions).</p>
                </div>
            </div>
            <div class="faq-item">
                <button class="faq-question">What's wrong with using Palantir?
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" width="20" height="20"><path stroke-linecap="round" stroke-linejoin="round" d="M19.5 8.25l-7.5 7.5-7.5-7.5" /></svg>
                </button>
                <div class="faq-answer">
                    <p>Palantir is a US defense contractor with deep ties to US intelligence agencies. Problems include:</p>
                    <ul>
                        <li>Subject to US jurisdiction and potential surveillance demands</li>
                        <li>Processes highly sensitive police and government data from EU citizens</li>
                        <li>Proprietary algorithms with no transparency or accountability</li>
                        <li>History of working closely with US intelligence agencies</li>
                        <li>Potential for backdoor access or data sharing with US government</li>
                    </ul>
                    <p>European police forces should use European software subject to European oversight.</p>
                </div>
            </div>
            <div class="faq-item">
                <button class="faq-question">Is GDPR enough to protect us?
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" width="20" height="20"><path stroke-linecap="round" stroke-linejoin="round" d="M19.5 8.25l-7.5 7.5-7.5-7.5" /></svg>
                </button>
                <div class="faq-answer">
                    <p>GDPR is necessary but not sufficient. While it provides strong privacy protections on paper, it cannot:</p>
                    <ul>
                        <li>Override US law when data is controlled by US companies</li>
                        <li>Prevent foreign intelligence surveillance</li>
                        <li>Guarantee European control of infrastructure</li>
                        <li>Protect against economic leverage by foreign tech companies</li>
                    </ul>
                    <p><strong>True sovereignty requires:</strong> European companies + European infrastructure + European law + GDPR enforcement.</p>
                </div>
            </div>
            <div class="faq-item">
                <button class="faq-question">How can I help?
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" width="20" height="20"><path stroke-linecap="round" stroke-linejoin="round" d="M19.5 8.25l-7.5 7.5-7.5-7.5" /></svg>
                </button>
                <div class="faq-answer">
                    <p>Several ways to support digital sovereignty:</p>
                    <ul>
                        <li><strong>Switch services:</strong> Use European alternatives for email, cloud, search</li>
                        <li><strong>Spread awareness:</strong> Share information about sovereignty risks</li>
                        <li><strong>Demand change:</strong> Ask your government to use European infrastructure</li>
                        <li><strong>Support European tech:</strong> Choose European companies when possible</li>
                        <li><strong>File FOI requests:</strong> Hold institutions accountable for data practices</li>
                    </ul>
                </div>
            </div>
