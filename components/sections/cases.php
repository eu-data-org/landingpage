                    <span class="case-year">2023</span>
                </div>
                <h3>German Police &amp; Palantir</h3>
                <p>Multiple German states signed contracts with Palantir for police data analysis software. Privacy activists challenged these contracts as violations of German and EU data protection law.</p>
                <div class="case-outcome"><strong>Outcome:</strong> Legal challenges ongoing, several contracts suspended.</div>
            </div>
            <div class="case-card">
                <div class="case-header">
                    <span class="case-country"><img src="/assets/img/flags/fr.png" alt="France" class="flag-icon"> France</span>
                    <span class="case-year">2021</span>
                </div>
                <h3>Health Data Hub &amp; Microsoft</h3>
                <p>France's Health Data Hub chose Microsoft Azure to host sensitive health data of 67 million French citizens, sparking massive controversy over CLOUD Act implications.</p>
                <div class="case-outcome"><strong>Outcome:</strong> Government pledged to migrate to European alternatives by 2023, timeline extended to 2025.</div>
            </div>
            <div class="case-card">
                <div class="case-header">
                    <span class="case-country">🇦🇹 Austria</span>
                    <span class="case-year">2021</span>
                </div>
                <h3>Austrian DSB Rules Google Analytics Illegal</h3>
                <p>The Austrian Data Protection Authority ruled that using Google Analytics violates GDPR because it transfers data to the US, where it's subject to surveillance.</p>
                <div class="case-outcome"><strong>Outcome:</strong> Similar rulings followed in France, Italy, and Denmark. Many EU organizations stopped using GA.</div>
