                <h3>Dutch Tax Office &amp; Amazon</h3>
                <p>The Dutch Tax and Customs Administration used Amazon Web Services to process sensitive citizen tax data. Following privacy concerns and GDPR violations, they were forced to migrate to EU-based alternatives.</p>
                <div class="case-outcome"><strong>Outcome:</strong> Migration to European cloud providers completed in 2024.</div>
            </div>
