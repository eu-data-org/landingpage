                    <h3>Our Principles</h3>
                    <ul class="about-principles">
                        <li><strong>No foreign jurisdiction</strong> — all our infrastructure is European-owned and European-hosted.</li>
                        <li><strong>No venture capital</strong> — self-funded, no investor pressure, no exit strategy.</li>
                        <li><strong>No tracking</strong> — we don't collect data we don't need. No cookies, no analytics.</li>
                        <li><strong>Open source first</strong> — we prefer and promote open-source tools wherever possible.</li>
                        <li><strong>Transparency</strong> — our findings, methods, and finances are published openly.</li>
                    </ul>
